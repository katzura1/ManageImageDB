    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        if(imgPath != null){
            InputStream img = null;
            try {
                img = new FileInputStream(imgPath);
                String sql = "update tbgambarbarang set Gambar = ? where idGambar = ? ";
                PreparedStatement ps = KelasKoneksi.getConnection().prepareStatement(sql);
                ps.setBlob(1, img);
                ps.setString(2, txtid.getText());
                if(ps.executeUpdate() == 1){
                    JOptionPane.showMessageDialog(null, "Sukses");
                }
                else{
                    JOptionPane.showMessageDialog(null, "id tidak ditemukan");
                }
            } catch (HeadlessException | FileNotFoundException | SQLException ex) {
                Logger.getLogger(JFrameImage.class.getName()).log(Level.SEVERE, null, ex);
            }  
        }
        else{
            JOptionPane.showMessageDialog(null, "gagal");
        }
        
        imgPath = null;
    }//GEN-LAST:event_btnUbahActionPerformed

