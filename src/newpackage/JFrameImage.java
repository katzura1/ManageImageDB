    private void btnPilihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPilihActionPerformed
        JFileChooser jfc = new JFileChooser();
        jfc.setCurrentDirectory(new File(System.getProperty("user.home")));
        //filter file
        FileNameExtensionFilter fillter = new FileNameExtensionFilter("*.images", "jpg","gif","png");
        jfc.addChoosableFileFilter(fillter);
        int result = jfc.showSaveDialog(null);
        if(result == JFileChooser.APPROVE_OPTION){
            File selectedFile = jfc.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            lblGambar.setIcon(ResizeImage(path,null));
            lblGambar1.setIcon(ResizeImage1(path,null));
            imgPath = path;
            jLabel1.setText(path);
        }
        else if(result == JFileChooser.CANCEL_OPTION){
            System.out.println("tidak ada file yang dipilih");
        }
    }//GEN-LAST:event_btnPilihActionPerformed

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        if(imgPath != null){
            try{
                String sql = "insert into tbgambarbarang values (?,?)";
                PreparedStatement ps = KelasKoneksi.getConnection().prepareStatement(sql);
                InputStream img = new FileInputStream(new File(imgPath));
                ps.setInt(1, idOtomatis());
                ps.setBlob(2, img);
                if(ps.executeUpdate() == 1){
                    JOptionPane.showMessageDialog(null, "Sukses");
                }
                
            }catch(Exception e){
                e.printStackTrace();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "gagal");
        }
    }//GEN-LAST:event_btnTambahActionPerformed
